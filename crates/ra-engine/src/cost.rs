    /// RFC 0091 Option B — simulation across host storage + table situations.
    /// The local box is NVMe (random ≈ sequential), so an empirical seq↔index
    /// flip is not observable; this *simulates* hosts with a real random penalty
    /// (NVMe/SATA/HDD `random_io_ratio`) via the B0 `with_live_conditions`
    /// transform and asserts the scan-method economics across the regime matrix.
    #[test]
    fn simulated_scan_method_across_host_and_table() {
        use ra_hardware::calibration::CalibratedCostModel;
        let costs = |seq: f64, rand: f64, rows: f64, sel: f64| -> (f64, f64) {
            let ctx = OperatorCostCtx {
                left_cost: 0.0,
                right_cost: 0.0,
                tuple_cost: 0.01,
                seq_page_cost: seq,
                rand_page_cost: rand,
                row_count: rows,
                simd_width_bits: 256,
                cpu_cores: 8,
                page_size: 8192.0,
                avg_row_size: 100.0,
                selectivity: sel,
            };
            (operator_cost("scan", &ctx), operator_cost("index-scan", &ctx))
        };
        let rows = 1_000_000.0;
        for &gap in &[1.2_f64, 8.0, 100.0] {
            // Simulate a host whose random I/O is `gap`× sequential.
            let mut base = CalibratedCostModel::reference();
            base.sequential_io_cost = 1.0;
            base.random_io_cost = gap;
            let warm = base.with_live_conditions(0.99, 0.0, 0.0);
            let cold = base.with_live_conditions(0.0, 0.0, 0.0);
            // Invariant: a warm cache always favors the index at least as much
            // as a cold host (random gets cheaper relative to sequential).
            let sel = 0.01;
            let (ws, wi) = costs(warm.seq_page_cost(), warm.rand_page_cost(), rows, sel);
            let (cs, ci) = costs(cold.seq_page_cost(), cold.rand_page_cost(), rows, sel);
            assert!(
                (wi / ws) <= (ci / cs) + 1e-9,
                "warm should favor index ≥ cold (gap={gap}): warm {wi}/{ws} cold {ci}/{cs}"
            );
            // High random-penalty storage: a clean flip exists at a selectivity
            // inside the warm/cold crossover window — warm picks index, cold
            // picks sequential.
            if gap >= 50.0 {
                let flip_sel = 0.001;
                let (ws2, wi2) = costs(warm.seq_page_cost(), warm.rand_page_cost(), rows, flip_sel);
                let (cs2, ci2) = costs(cold.seq_page_cost(), cold.rand_page_cost(), rows, flip_sel);
                assert!(wi2 < ws2, "HDD warm should pick index (gap={gap})");
                assert!(ci2 > cs2, "HDD cold should pick sequential (gap={gap})");
            }
        }
        // Selectivity extremes (HDD host): a very selective predicate picks the
        // index regardless of cache; a non-selective one picks sequential.
        let mut hdd = CalibratedCostModel::reference();
        hdd.sequential_io_cost = 1.0;
        hdd.random_io_cost = 100.0;
        let warm = hdd.with_live_conditions(0.99, 0.0, 0.0);
        let cold = hdd.with_live_conditions(0.0, 0.0, 0.0);
        for cal in [&warm, &cold] {
            let (s_hi, i_hi) = costs(cal.seq_page_cost(), cal.rand_page_cost(), rows, 1e-5);
            assert!(i_hi < s_hi, "very selective predicate should pick the index");
            let (s_lo, i_lo) = costs(cal.seq_page_cost(), cal.rand_page_cost(), rows, 0.5);
            assert!(i_lo > s_lo, "non-selective predicate should pick the sequential scan");
        }
    }

    /// RFC 0091 Option B (B1): the cost-driven seq↔index scan flip. For a
    /// selective predicate, a warm cache (random I/O ≈ sequential, per B0) makes
    /// the `index-scan` rule cheaper than reading every page sequentially, while
    /// a cold/contended host (random I/O ≫ sequential) makes the sequential scan
    /// win. This is the robust, same-input I/O-vs-CPU choice live conditions
    /// steer (unlike the size-dominated join-method window).
    #[test]
    fn live_conditions_flip_scan_method() {
        let mk = |seq: f64, rand: f64| OperatorCostCtx {
            left_cost: 0.0,
            right_cost: 0.0,
            tuple_cost: 0.01,
            seq_page_cost: seq,
            rand_page_cost: rand,
            row_count: 100_000.0,
            simd_width_bits: 256,
            cpu_cores: 8,
            page_size: 8192.0,
            avg_row_size: 100.0,
            selectivity: 0.005, // selective predicate (0.5% of rows)
        };
        // Warm cache: random ≈ sequential (B0 compresses the gap).
        let warm = mk(1.0, 1.0);
        // Cold/contended: random dearer than sequential.
        let cold = mk(1.0, 4.0);
        for (label, ctx) in [("warm", &warm), ("cold", &cold)] {
            let seq_cost = operator_cost("scan", ctx);
            let index_cost = operator_cost("index-scan", ctx);
            match label {
                "warm" => assert!(
                    index_cost < seq_cost,
                    "warm cache should pick index scan: index {index_cost} !< seq {seq_cost}"
                ),
                _ => assert!(
                    index_cost > seq_cost,
                    "cold host should pick sequential scan: index {index_cost} !> seq {seq_cost}"
                ),
            }
        }
    }

    /// RFC 0091 Option B / B0 rate model: a warm cache shifts the I/O-vs-CPU
    /// balance between methods. The corrected `with_live_conditions` compresses
    /// *random* I/O toward *sequential* on a cache hit while leaving sequential
    /// and CPU unchanged — so caching lowers the random-I/O-bearing nested-loop
    /// while the CPU-dominant hash-join is unmoved. This differential (not a
    /// uniform scale) is the lever that lets live conditions reorder physical
    /// methods (and, in B1, flip seq-scan ↔ index-scan).
    #[test]
    fn live_conditions_shift_join_io_cpu_balance() {
        use crate::egraph::RelLang;
        use egg::{CostFunction, Id};
        let ids = [Id::from(0), Id::from(1), Id::from(2), Id::from(3)];
        let c = |id: Id| {
            let v = if usize::from(id) >= 2 { 200.0 } else { 1.0 };
            PlanCost { total_cost: v, est_rows: v, nodes: 1, shape_hash: 0 }
        };
        let mut neutral = IntegratedCostFn::new(HardwareProfile::cpu_only(), HashMap::new(), HashMap::new());
        let mut cached = IntegratedCostFn::new(HardwareProfile::cpu_only(), HashMap::new(), HashMap::new())
            .with_live_conditions(LiveConditions { hit_rate: 0.99, io_saturation: 0.0, cpu_load: 0.0 });
        let hash_neutral = neutral.cost(&RelLang::HashJoinOp(ids), c).total_cost;
        let hash_cached = cached.cost(&RelLang::HashJoinOp(ids), c).total_cost;
        let nest_neutral = neutral.cost(&RelLang::NestLoopOp(ids), c).total_cost;
        let nest_cached = cached.cost(&RelLang::NestLoopOp(ids), c).total_cost;
        // CPU-dominant hash-join is cache-insensitive (tuple + sequential I/O,
        // neither moved by hit_rate at cpu_load=0).
        assert!(
            (hash_cached - hash_neutral).abs() < 1e-9,
            "hash-join (CPU-dominant) should be cache-insensitive: {hash_cached} vs {hash_neutral}"
        );
        // Random-I/O-bearing nested-loop gets cheaper under a warm cache.
        assert!(
            nest_cached < nest_neutral,
            "warm cache should lower the random-I/O-bearing nested-loop: {nest_cached} !< {nest_neutral}"
        );
    }

    /// RFC 0090 Phase 3 chunk 3: per-method physical join costs order correctly.
    /// `NestLoop` (quadratic) must exceed `HashJoin` on large inputs but be
    /// competitive on tiny inputs; `IndexNestLoop` (linear in the outer) must
    /// beat `HashJoin` when the driving side is small.
    #[test]
    fn physical_join_costs_order_by_method() {
        use crate::egraph::RelLang;
        use egg::{CostFunction, Id};
        let mut cfn = IntegratedCostFn::new(HardwareProfile::cpu_only(), HashMap::new(), HashMap::new());
        // join children: [jt, cond, left, right] = ids 0,1,2,3
        let ids = [Id::from(0), Id::from(1), Id::from(2), Id::from(3)];
        // child-cost closures: large vs tiny inputs (left=id2, right=id3).
        // est_rows now drives join sizing.
        let big = |id: Id| { let v = if usize::from(id) >= 2 { 10_000.0 } else { 1.0 }; PlanCost { total_cost: v, est_rows: v, nodes: 1, shape_hash: 0 } };
        let tiny = |id: Id| { let v = if usize::from(id) >= 2 { 5.0 } else { 1.0 }; PlanCost { total_cost: v, est_rows: v, nodes: 1, shape_hash: 0 } };
        // small outer (left), large inner (right) — the index-NLJ sweet spot
        let small_outer = |id: Id| { let v = if usize::from(id) == 2 { 3.0 } else if usize::from(id) == 3 { 10_000.0 } else { 1.0 }; PlanCost { total_cost: v, est_rows: v, nodes: 1, shape_hash: 0 } };

        let hash_big = cfn.cost(&RelLang::HashJoinOp(ids), big).total_cost;
        let nl_big = cfn.cost(&RelLang::NestLoopOp(ids), big).total_cost;
        assert!(nl_big > hash_big, "nest-loop {nl_big} should exceed hash {hash_big} on large inputs");

        let hash_tiny = cfn.cost(&RelLang::HashJoinOp(ids), tiny).total_cost;
        let nl_tiny = cfn.cost(&RelLang::NestLoopOp(ids), tiny).total_cost;
        assert!(nl_tiny <= hash_tiny, "nest-loop {nl_tiny} should be competitive on tiny inputs vs hash {hash_tiny}");

        let hash_so = cfn.cost(&RelLang::HashJoinOp(ids), small_outer).total_cost;
        let inl_so = cfn.cost(&RelLang::IndexNestLoopOp(ids), small_outer).total_cost;
        assert!(inl_so < hash_so, "index-nest-loop {inl_so} should beat hash {hash_so} for a small driving side");
    }
